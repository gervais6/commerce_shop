Les frais de livraison sont à la charge de l’acheteur et sont inclus dans la facturation totale du produit conformément à notre grille tarifaire de livraison.
<br><br>
Pour les livraisons hors de la région de Dakar, le client devra payer intégralement, à l’avance, les frais de livraison.
<br><br>
Les délais de livraison ne sont donnés qu’à titre indicatif et GMBOLOMARKET déploiera tous les moyens nécessaires pour livrer le produit commandé dans les délais prévus.
<br><br>
Si aucun délai n’a été prévu, nous sommes tenus de vous livrer le produit dans un délai de 30 jours conformément à la règlementation en vigueur.
<br><br>
En cas de retard ou d’impossibilité de livrer le produit dû à son indisponibilité, l’acheteur sera informé sans délai par courriel, SMS ou par appel téléphonique.
<br><br>
Les risques liés au transport sont à la charge de GMBOLOMARKET à compter du moment où les articles quittent les locaux de la société. L’acheteur est tenu de vérifier en présence du livreur, l’état de l’emballage de la marchandise et son contenu à la livraison. En cas de dommage pendant le transport, toute protestation doit être effectuée auprès de GMBOLOMARKET dès réception de la livraison par courriel à l’adresse info@gmbolomarket.com ou par appel téléphonique au 77 100 18 97
<br><br>

La livraison se fait sous réserve de conditions normales d’accessibilité du lieu et du domicile de l’acheteur.
<br><br>
Le client sera contacté par le livreur au moment de la livraison pour la confirmation de l’adresse de livraison.
<br><br>
Le Client s’engage à être présent au jour et aux heures qu’il aura choisies. En cas d’impossibilité de livrer résultant de l’absence du Client au cours du créneau horaire choisi, une nouvelle livraison sera programmée. Dans ce cas, GMBOLOMARKET pourrait exiger le paiement complet des frais et/ou du produit commandé avant toute opération de livraison.

<br><br><br><br>
<strong>Article 7 : FACTURATION ET PAIEMENT</strong>
<br><br>
La monnaie de facturation est le Francs CFA.
<br><br>
Le paiement se fait à la livraison et selon deux modes :
<br><br>
Paiement au comptant dès la réception de la commande
Paiement via les mobiles-money (Orange Money et Wave).
Actuellement le paiement via les mobiles money se font par le biais des fonctionnalités de transferts.

