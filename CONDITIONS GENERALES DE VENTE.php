Dès la validation, Le client sera ensuite contacté par téléphone pour une confirmation de sa commande avec une précision du lieu exact de livraison.

Le client sera aussi contacté par le même canal par  GMBOLOMARKET afin de lui fournir toutes informations liées à sa commande notamment en cas de rupture de stock.
