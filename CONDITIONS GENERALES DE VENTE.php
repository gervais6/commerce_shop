<br><br>
L’Acheteur dispose d’un délai de rétractation de 7 jours francs à compter de la livraison des produits pour les retourner à GMBOLOMARKET à condition que les produits soient retournés en parfait état dans leurs emballages d’origine.
<br><br>
En cas d’exercice de son droit de rétractation, l’acheteur dispose de trois solutions :
<br><br>
Echanger le produit avec un autre ayant la même valeur
Demander remboursement des montants payés
