<br><br>
Les produits endommagés, salis ou incomplets après livraison, ne seront ni repris, ni échangés.
<br><br>
Si le bon produit a été livré, les frais de retour seront exclusivement à la charge de l’acheteur.
<br><br>
Toutefois, conformément à la règlementation sur le commerce électronique, nous vous rappelons que le droit de rétractation ne s’applique pas aux produits confectionnés selon les spécifications de l’acheteur ou qui, du fait de leur nature, ne peuvent pas être réexpédies parce que susceptibles de se détériorer ou de se périmer rapidement.
